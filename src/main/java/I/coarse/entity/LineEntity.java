package I.coarse.entity;

import java.awt.Point;

public class LineEntity {

	private Point begin;
	private Point end;
	
	public Point getBegin() {
		return begin;
	}
	
	public void setBegin(Point begin) {
		this.begin = begin;
	}
	
	public Point getEnd() {
		return end;
	}
	
	public void setEnd(Point end) {
		this.end = end;
	}
	
	
}
